
let x=10
//let is used to declare a variable that can be reassigned a new value , it is a block scoped variable
var y=20
//var is used to declare a variable that can be reassigned a new value , it is a function scoped variable
const z=30
//const is used to declare a variable that cannot be reassigned a new value , it is a block scoped variable
console.log(x, y, z);