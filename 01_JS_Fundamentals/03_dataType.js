//Primitive Data Types:

//Number: 
console.log(12)
console.log(12.34)
console.log(0);
console.log(-0);


//String:
console.log('hello world');
console.log(`hello world`); //backtick

//Boolean:
console.log(true);
console.log(false);

//Undefined:
console.log(undefined);

//Null:
console.log(null);

//Object: key value pairs 
console.log({name: 'John', age: 30});

//symbol:
console.log(Symbol('hello'));