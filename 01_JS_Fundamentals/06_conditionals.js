//if else
let age = 18;
if(age>=18){
    console.log('You are eligible to vote');
}
else{
    console.log('You are not eligible to vote');
}

//else if
let marks = 75;
if(marks>=90){
    console.log('A grade');
}
else if(marks>=80){
    console.log('B grade');
}
else if(marks>=70){
    console.log('C grade');
}
else if(marks>=60){
    console.log('D grade');
}
else{
    console.log('F grade');
}